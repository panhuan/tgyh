function Mission:calcBallLevelUp(mission, ballId, curLevel, curCount)
	if not self.missionMap[mission] or not self.missionMap[mission].skillLevelUpCosts then
		return nil
	end
	local tb = self.missionMap[mission].skillLevelUpCosts[ballId]
	local level = 0
	for l = curLevel+1, #tb do
		local needCount = tb[l]
		if curCount >= needCount then
			level = level + 1
			curCount = curCount - needCount
		end
	end
	return level, curCount
end

